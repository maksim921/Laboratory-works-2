a = "Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье"

n = int(input("Сколько выходных вам нужно? "))
if not 0 <= n <= 7:
    print("Так не получится")
elif n == 0:
    print("Ваши выходные дни:\nВаши рабочие дни: ", ", ".join(a))
else:
    print("Ваши выходные дни: ", ", ".join(a[-n:]))
    print("Ваши рабочие дни: ", ", ".join(a[:-n]))
